package org.cryptomator.ui.preferences;

public enum SelectedPreferencesTab {
	/**
	 * Let the controller decide which tab to show.
	 */
	ANY,

	/**
	 * Show general tab
	 */
	GENERAL,

	/**
	 * Show volume tab
	 */
	VOLUME,

	/**
	 * Show updates tab
	 */
	UPDATES,

	/**
	 * Show donation key tab
	 */
	DONATION_KEY,

	/**
	 * Show about tab
	 */
	ABOUT,
}
